/*jslint anon:true, sloppy:true, nomen:true*/
YUI.add('RiderBinderIndex', function(Y, NAME) {

/**
 * The RiderBinderIndex module.
 *
 * @module RiderBinderIndex
 */

    /**
     * Constructor for the RiderBinderIndex class.
     *
     * @class RiderBinderIndex
     * @constructor
     */
    Y.namespace('mojito.binders')[NAME] = {

        /**
         * Binder initialization method, invoked after all binders on the page
         * have been constructed.
         */
        init: function(mojitProxy) {
            this.mojitProxy = mojitProxy;

	        this.mojitProxy.invoke('test', {params: {testParam: 'test param'}}, function() { console.log('invoked controller\'s test method'); });
        },

        /**
         * The binder method, invoked to allow the mojit to attach DOM event
         * handlers.
         *
         * @param node {Node} The DOM node to which this mojit is attached.
         */
        bind: function(node) {
            var me = this;
            this.node = node;
            node.all('dt').on('mouseenter', function(evt) {
                var dd = '#dd_' + evt.target.get('text');
                me.node.one(dd).addClass('sel');
            });
            node.all('dt').on('mouseleave', function(evt) {
                var dd = '#dd_' + evt.target.get('text');
                me.node.one(dd).removeClass('sel');
            });
        }

    };

}, '0.0.1', {requires: ['event-mouseenter', 'mojito-client']});
